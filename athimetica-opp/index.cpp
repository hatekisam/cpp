#include <iostream>

int main()
{

    // Addtion
    int a = 6 + 7;
    a++;
    a += 10;
    std::cout << a << std::endl;
    // Subtraction
    int b = 16 - 7;
    b--;
    a -= 2;
    std::cout << b << std::endl;
    // Multiplication
    int c = 2 * 3;
    c *= 2;
    std::cout << c << std::endl;
    // Division
    int d = 16 / 2;
    d /= 2;
    std::cout << d << std::endl;
    // Modulus
    int e = 16 % 2;
    e %= 2;
    std::cout << e << std::endl;

    return 0;
}